@app.route('/predictdata', methods=['GET', 'POST'])
def predict_datapoint():
    if 'user_id' not in session:
        flash("Please login first!", "warning")
        return redirect(url_for('login'))

    if request.method == 'GET':
        return render_template('home.html')

    gender = request.form.get('gender')
    race_ethnicity = request.form.get('ethnicity')
    parental_education = request.form.get('parental_level_of_education')
    lunch = request.form.get('lunch')
    test_prep = request.form.get('test_preparation_course')
    reading_score = float(request.form.get('reading_score'))
    writing_score = float(request.form.get('writing_score'))

    data = CustomData(
        gender=gender,
        race_ethnicity=race_ethnicity,
        parental_level_of_education=parental_education,
        lunch=lunch,
        test_preparation_course=test_prep,
        reading_score=reading_score,
        writing_score=writing_score
    )

    pred_df = data.get_data_as_data_frame()
    predict_pipeline = PredictPipeline()
    results = predict_pipeline.predict(pred_df)
    predicted_score = round(float(results[0]), 2)

    # ✅ Save prediction to Supabase
    try:
        new_prediction = Prediction(
            user_id=session['user_id'],
            username=session['username'],
            gender=gender,
            race_ethnicity=race_ethnicity,
            parental_education=parental_education,
            lunch=lunch,
            test_prep=test_prep,
            reading_score=reading_score,
            writing_score=writing_score,
            predicted_score=predicted_score
        )
        db.session.add(new_prediction)
        db.session.commit()
    except Exception as e:
        db.session.rollback()
        print("Error saving prediction:", e)

    
    # ✅ Send email only when running locally (Render blocks Gmail port)
    if not os.environ.get('RENDER'):
        import threading
        TEACHER_EMAIL = "pratikksecondaryacc@gmail.com"
        thread = threading.Thread(target=send_prediction_email, args=(
            TEACHER_EMAIL,
            session['username'],
            predicted_score,
            reading_score,
            writing_score
        ))
        thread.daemon = True
        thread.start()

    return render_template('home.html', results=predicted_score)


