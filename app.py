# ------------------- Database Models -------------------

class User(db.Model):
    __tablename__ = 'users'
    id = db.Column(db.Integer, primary_key=True)
    username = db.Column(db.String(100), unique=True, nullable=False)
    email = db.Column(db.String(120), unique=True, nullable=False)
    password = db.Column(db.String(200), nullable=False)

    def __repr__(self):
        return f'<User {self.username}>'


class Prediction(db.Model):
    __tablename__ = 'predictions'
    id = db.Column(db.Integer, primary_key=True)
    user_id = db.Column(db.Integer, nullable=False)
    username = db.Column(db.String(100), nullable=False)
    gender = db.Column(db.String(20))
    race_ethnicity = db.Column(db.String(50))
    parental_education = db.Column(db.String(100))
    lunch = db.Column(db.String(50))
    test_prep = db.Column(db.String(50))
    reading_score = db.Column(db.Float)
    writing_score = db.Column(db.Float)
    predicted_score = db.Column(db.Float)
    created_at = db.Column(db.DateTime, default=get_ist_time)

    def __repr__(self):
        return f'<Prediction {self.username} - {self.predicted_score}>'


